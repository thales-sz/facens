## Operadores ##

# Aritméticos ( + - * / % )

print("Soma", 5 + 2)
print("Multiplicação", 5 * 2)
print("Subtração", 5 - 2)
print("Divisão", 5 / 2)
print("Divisão Inteira", 5 // 2)
print("Módulo (Resto)", 5 % 2)
print("Exponenciação", 5 ** 2)

# Relacionais  ( < > <= >= == )

print("Maior que", 5 > 2)
print("Menor que", 5 < 2)
print("Maior ou igual a", 5 >= 2)
print("Menor ou igual a", 5 <= 2)
print("Igual a", 5 == 2)

# Lógicos ( or and not)
print("E", True & False) # Pode ser escrito como True and False
print("OU", True | False) # Pode ser escrito como True or False
print("Negação", not True)
print("Exclusivo", True ^ True) 
