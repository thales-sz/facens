grade1 = int(input('Digite a primeira nota:'))
grade2 = int(input('Digite a segunda nota:'))

average = (grade1 + grade2) / 2

if average >= 7:
  print('Aprovado')
elif average < 7 and average >= 5:
  print('Exame')
else:
  print('Reprovado')


